    private void popuniVozila() {
        cmbVozilo.removeAllItems();
        ArrayList<Vozilo> lista = Kontroler.getInstanca().vratiListuVozila();

        for (Vozilo vozilo : lista) {
            cmbVozilo.addItem(vozilo);
        }
    }

    private void popuniPodatkeVozila() {
        Vozilo vozilo = (Vozilo) cmbVozilo.getSelectedItem();
        if (vozilo != null) {
            txtGodinaProizvodnje.setText(vozilo.getGodinaProzivodnje() + "");
            txtVlasnikVozila.setText(vozilo.getIme() + " " + vozilo.getPrezime());
        }
    }

    private void popuniVrsteOsiguranja() {
        cmbVrsteOsiguranja.removeAllItems();
        ArrayList<VrstaOsiguranja> lista = Kontroler.getInstanca().getListaVrsteOsiguranja();

        for (VrstaOsiguranja vrstaOsiguranja : lista) {
            cmbVrsteOsiguranja.addItem(vrstaOsiguranja);
        }
    }

    private void popuniPodatkeVrstaOsiguranja() {
        VrstaOsiguranja vs = (VrstaOsiguranja) cmbVrsteOsiguranja.getSelectedItem();
        if (vs != null) {
            txtCenaVrste.setText(vs.getCena() + "");
        }
    }

