    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String dat = txtDatum.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        try {
            Date datum = sdf.parse(dat);
            Vozilo v = (Vozilo) cmbVozilo.getSelectedItem();

            ArrayList<Osiguranje> listaOsiguranja = Kontroler.getInstanca().vratiOsiguranja(v);
            for (Osiguranje osiguranje : listaOsiguranja) {
                Calendar calendar = Calendar.getInstance();
                calendar.setTime(osiguranje.getDatumPocetka());
                int godinaPocetka = calendar.get(Calendar.YEAR);

                Calendar cal = Calendar.getInstance();
                cal.setTime(datum);
                int brojGodina = cal.get(Calendar.YEAR);

                if (brojGodina == godinaPocetka) {
                    JOptionPane.showMessageDialog(this, "Godina pocetka osiguranja");
                    return;
                }
            }

            // trenutna godina
            int god = Calendar.getInstance().get(Calendar.YEAR);
            int razlika = god - v.getGodinaProzivodnje();

            ModelTabeleVrsta tvs = (ModelTabeleVrsta) tabelaVrstaOsiguranja.getModel();
            ArrayList<VrstaOsiguranja> vrste = tvs.getVrste();
            double premija = 0;
            for (VrstaOsiguranja vrstaOsiguranja : vrste) {
                premija += razlika * vrstaOsiguranja.getCena();
            }
            Korisnik ulogovaniKorisnik = ((GlavnaForma) getParent()).getUlogovaniKorisnik();

            Osiguranje o = new Osiguranje(-1, v, datum, new Date(), ulogovaniKorisnik.getImePrezime(), premija);
            boolean uspesno = Kontroler.getInstanca().sacuvajOSiguranje(o);
            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Uspesno sacuvano u bazi");
            } else {
                JOptionPane.showMessageDialog(this, "Nije sacuvano");
            }
        } catch (ParseException ex) {
            Logger.getLogger(UnosOsiguranja.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Datum je u losem pogresnom formatu");
        }


    }//GEN-LAST:event_btnSacuvajActionPerformed


